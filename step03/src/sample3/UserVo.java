package sample3;

public class UserVo {
	private String userName;

	//생성자 함수
	public UserVo(String userName) {
		super();
		this.userName = userName;
	}

	//getter setter
	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}
	
	
	
	
}
